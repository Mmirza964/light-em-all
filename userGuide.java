/* 
UserGuide for LighEmAll:
The point of this game is to connect every wire to the center power grid.
You can move the powergrid by using the arrow keys. 
There is a timer at the top, that counts up in seconds to show 
how long you have been playing this game.
There is also a moves counter, that increments by 1 every time
a tile is rotated.
Along with this you can rotate the tiles clockwise by clicking on them
with the left button. You cannot move the power grid onto a wire that is
not connected.
Once all the wires connect to the power grid you have won, and
a congratulations message will appear.
*/